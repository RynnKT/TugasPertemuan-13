    private void btnBuatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuatActionPerformed
        if (tfUser.getText().equals("") || jPasswordField1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else {
            String user, pw;
            user = tfUser.getText();
            pw = jPasswordField1.getText();

            EntityManagerFactory emf = Persistence.createEntityManagerFactory("Pertemuan_13PU");
            EntityManager em = emf.createEntityManager();
            em.getTransaction().begin();

            Akun y = em.find(Akun.class, user);
            if (y != null) {
                JOptionPane.showMessageDialog(null, "Username sudah ada, gunakan username lain");
                bersih();
                tfUser.requestFocus();
            } else {
                Akun x = new Akun();
                x.setUsername(tfUser.getText());
                x.setPassword(jPasswordField1.getText());
                em.persist(x);
                em.getTransaction().commit();

                JOptionPane.showMessageDialog(null, "Sukses dibuat");
                bersih();
                this.dispose();
            }
            em.close();
            emf.close();
        }
    }//GEN-LAST:event_btnBuatActionPerformed

